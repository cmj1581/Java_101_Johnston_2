public class Person {
    private int age;
    private String name;

    public Person() {
      age = 0;
      name = "John Doe";
    }

    public void setAge(int a) {
      this.age = a;
    }
    public void setName(String n) {
      this.name = n;
    }

    public int getAge() { return age; }
    
    public String getName() { return name; }
    


}

